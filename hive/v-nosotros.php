                                     ¿Cuál es el costo/km real de mis neumáticos? 
                                    ¿Cuántos neumáticos debería comprar cada mes?
                                    ¿Las marcas que estoy comprando efectivamente son tan buenas como prometen?
                                     Entre otras, resultaban muy difíciles de responder con la información manejada, la cual era pobre en cantidad y calidad de la data. La poca información recopilada se capturaba en forma manual y se digitaba a planillas excel.
                                     Después de buscar en el mercado soluciones que resolvieran la problemática, nos dimos cuenta de que no existía tecnología o productos similares por lo que decidimos desarrollar HIVETIRE, la cual resultó resolver una necesidad real de mercado. Además, fuimos capaces de incorporar tecnología para realizar inspecciones digitales de los neumáticos, herramientas que vía bluetooth transmitían la información directamente a nuestra aplicación móvil y al mismo tiempo a la plataforma web.
                                </p> 

